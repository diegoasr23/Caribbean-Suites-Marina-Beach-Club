    public void validarCampos() {
        JTextField containerTextField[] = {responsableTextField, serialBrazaleteTextField, inmuebleTextField,
            serialAutorizacionTextField, montoPagadoTextField, refPagoTextField};

        JTextField containetTextFieldPropietario[] = {responsableTextField, serialBrazaleteTextField, inmuebleTextField};

        JRadioButton container[] = {propietarioRdoBtn, invitadoRdoBtn, alquiladoRdiobtn};

        Arrays.asList(container).forEach(c -> {
            if (!propietarioRdoBtn.isSelected() && !invitadoRdoBtn.isSelected() && !alquiladoRdiobtn.isSelected()) {
                JOptionPane.showMessageDialog(null, "DEBES SELECCIONAR UNA CONDICION", "ERROR", JOptionPane.ERROR_MESSAGE);
                throw new NullPointerException();
            }

        });

        if (condicion == Condicion.PROPIETARIO || (condicion == Condicion.INVITADO && representaIngresoTglBtn.getText().equals("NO"))) {
            Arrays.asList(containetTextFieldPropietario).forEach(c -> {
                if (c.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "DEBES LLENAR TODOS LOS CAMPOS", "ERROR", JOptionPane.ERROR_MESSAGE);
                    throw new NullPointerException();
                }
            });
        } else {

            Arrays.asList(containerTextField).forEach(c -> {
                if (c.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "DEBES LLENAR TODOS LOS CAMPOS", "ERROR", JOptionPane.ERROR_MESSAGE);
                    throw new NullPointerException();
                }
            });
        }

    }
