    public void evaluarCondicionSeleccionada(Condicion condicion) {
        switch (condicion) {
            case PROPIETARIO -> {
                condicionSelected = true;
                representaIngresoTglBtn.setText("NO");
                representaIngresoTglBtn.setEnabled(false);
                this.condicion = Condicion.PROPIETARIO;
            }

            case ALQUILADO -> {
                condicionSelected = true;
                representaIngresoTglBtn.setText("SI");
                representaIngresoTglBtn.setEnabled(false);
                representaIngresoTglBtn.setBackground(new Color(153, 255, 153));
                representaIngresoTglBtn.setForeground(new Color(0, 0, 51));
                this.condicion = Condicion.ALQUILADO;
            }
            default -> {
                condicionSelected = true;
                representaIngresoTglBtn.setText("SI");
                representaIngresoTglBtn.setEnabled(true);

                this.condicion = Condicion.INVITADO;
            }
        }
    }

