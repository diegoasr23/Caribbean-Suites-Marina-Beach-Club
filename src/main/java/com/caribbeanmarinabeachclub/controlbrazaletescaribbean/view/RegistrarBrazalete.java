    private void representaIngresoTglBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_representaIngresoTglBtnActionPerformed
        JComponent container[] = {formaPagoComboBox, montoPagadoTextField, refPagoTextField};
        Arrays.asList(container).forEach(a -> {
            a.setEnabled(representaIngresoFlag);
        });

        if (representaIngresoTglBtn.getText().equalsIgnoreCase("SI")) {
            representaIngresoTglBtn.setText("NO");
            representaIngresoTglBtn.setBackground(new Color(204, 0, 0));
            representaIngresoTglBtn.setForeground(new Color(255, 255, 255));
            representaIngresoFlag = true;

        } else {
            representaIngresoTglBtn.setText("SI");
            representaIngresoTglBtn.setBackground(new Color(153, 255, 153));
            representaIngresoTglBtn.setForeground(new Color(0, 0, 51));
            representaIngresoFlag = false;
        }

    }//GEN-LAST:event_representaIngresoTglBtnActionPerformed

